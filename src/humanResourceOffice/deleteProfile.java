    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        String a1 = auth1.getText();
        String a2 = auth2.getText();
        int row = table.getSelectedRow();
        if(row<0){
            JOptionPane.showMessageDialog(this,"Please select row to delete");
            return;
                    
        }
        
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        employeeProfile selectedep = (employeeProfile)model.getValueAt(row, 0);
        String A1 = String.valueOf(selectedep.getId());
        String A2 = String.valueOf(selectedep.getCellPhoneNumber());
        
        if (a1.isEmpty()|| a2.isEmpty())
        {
            JOptionPane.showMessageDialog(this,"Please enter credentials Employee ID and Cell Phone Number");
        }
        
        else if (a1.equals(A1) && a2.equals(A2)){
        
        emp.deleteEmp(selectedep);
        JOptionPane.showMessageDialog(this,"Employee Profile deleted");
        auth1.setText("");
        auth2.setText("");
        populateTable();
        }
        else
        {
           JOptionPane.showMessageDialog(this,"Incorrect credentials"); 
        }

    }//GEN-LAST:event_deleteActionPerformed

