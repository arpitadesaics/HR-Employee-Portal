    private void populateTable() {
          DefaultTableModel model = (DefaultTableModel) table.getModel();
          model.setRowCount(0);
        
        for (employeeProfile ep:emp.getEmp()){
            
            Object[] row = new Object[2];
            row[0] = ep;
            row[1] = ep.getTeamInfo();
            
            model.addRow(row);
            
        }
        
    }
